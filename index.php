                <p class="lead">REBA é uma ferramenta de Avaliação Rápida de Corpo Inteiro que está sendo desenvolvida para investigar atividades dinâmicas onde há risco de distúrbios musculoesqueléticos relacionados ao trabalho. Deve ser usado para investigar tarefas para as quais o RULA (Avaliação Rápida do Membros Superiores) não foi projetado.</p>
