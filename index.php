                <h2>Formulário Equação de levantamento manual de carga NIOSH</h2>
                <p class="lead">A equacação de levantamento do NIOSH (Instituto Nacional de Segurança e Saúde Ocupacional) é uma ferramenta usada por profissionais de saúde e segurança ocupacional para avaliar os riscos do manuseio manual de materiais associados às tarefas de levantamento e abaixamento no local de trabalho.
				Uma tarefa de levantamento é definida como o ato de agarrar manualmente um objeto com as duas mãos e mover o objeto verticalmente sem ajuda mecânica. A equação de levantamento do NIOSH considera várias variáveis de tarefa de trabalho para determinar práticas e diretrizes de levantamento seguras.</p>
