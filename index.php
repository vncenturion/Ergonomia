                <h2>Formulário Rapid Upper Limb Assessment - RULA©</h2>
                <p class="lead">RULA (Rapid Upper Limb Assessment ou Avaliação Rápida de Membros Superiores) é um método de pesquisa desenvolvido para uso em investigações ergonômicas de locais de trabalho onde distúrbios de membros superiores relacionados ao trabalho são relatados. Esta ferramenta não requer nenhum equipamento especial para fornecer uma avaliação rápida das posturas do pescoço, tronco e membros superiores juntamente com a função muscular e as cargas externas experimentadas pelo corpo. Um sistema de codificação é usado para gerar uma lista de ações que indica o nível de intervenção necessária para reduzir os riscos de lesões devido à carga física no operador.</p>
